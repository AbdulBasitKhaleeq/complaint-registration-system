<h2>Methodology of Project</h2>
<p style="font-size:16px;">
Working method of this application will have 4 steps.
<ol>
<li>A citizen can book a general complaint by signing up to the application by providing their:</li>
<ul>
<li>- Name</li>
<li>- Contact Number</li>
<li>- CNIC number</li> 
<li>- Address</li>
</ul>
<li>GPS will locate the location of the person (If some changes required then user can change the location) and generate the address</li>
<li>User can also provide an image of that problem along the complaint (Garbage, Open Manhole, Sewage Issue, etc.)</li>
</ol>
<b>System will generate the following thing for the user automatically:</b>
<ul>
<li>Complaint Number, an ID against which the progress could be tracked
</li><li>Allocation of approximate time in which workers will reach the place and time required to resolve the issue.</li>
  </ul>
